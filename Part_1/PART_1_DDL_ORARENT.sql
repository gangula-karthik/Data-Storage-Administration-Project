CREATE TABLE ORARENT_CUSTOMERS 
  (CUSTOMER_ID NUMBER(10) CONSTRAINT NOT NULL, 
   LAST_NAME VARCHAR2(25) CONSTRAINT NOT NULL,
   FIRST_NAME VARCHAR2(25) CONSTRAINT NOT NULL, 
   HOME_PHONE VARCHAR2(12) CONSTRAINT NOT NULL,
   ADDRESS VARCHAR2(100) CONSTRAINT NOT NULL, 
   CITY VARCHAR2(30) CONSTRAINT NOT NULL,
   STATE VARCHAR2(2) CONSTRAINT NOT NULL, 
   EMAIL VARCHAR2(25),
   CELL_PHONE VARCHAR2(12),
   CONSTRAINT customers_customer_id_pk PRIMARY KEY(CUSTOMER_ID)
);
